package assignment6_000901300;

/**
 * Represents a fairy in the fantasy world, extending the Inhabitant class.
 * Author: Devang Bhojane, 000901300
 */
public class Fairy extends Inhabitant {
    /**
     * Constructor to initialize a fairy with a name only.
     *
     * @param name The name of the fairy.
     */
    public Fairy(String name) {
        super(name);
    }

    /**
     * Returns a string representation of the fairy.
     *
     * @return A formatted string representing the fairy's attributes.
     */
    public String toString() {
        return super.toString();
    }
}
